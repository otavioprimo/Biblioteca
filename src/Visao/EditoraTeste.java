    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        CtrlEditora.editar(jtxtNomeEditora.getText().trim(), jtxtEmailEditora.getText().trim(), jtxtCnpjEditora.getText().trim(), idEditoraAtual);
        listarTabelaEditora();
        jtxtId.setText("");
        jtxtNomeEditora.setText("");
        jtxtEmailEditora.setText("");
        jtxtCnpjEditora.setText("");
        nomeEditoraAtual = "";
    }//GEN-LAST:event_btnEditarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        CtrlEditora.excluir(idEditoraAtual, nomeEditoraAtual);
        listarTabelaEditora();
        jtxtId.setText("");
        jtxtNomeEditora.setText("");
        jtxtEmailEditora.setText("");
        jtxtCnpjEditora.setText("");
        nomeEditoraAtual = "";
    }//GEN-LAST:event_btnExcluirActionPerformed

    private void btnNovoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNovoActionPerformed
        CtrlEditora.salvar(jtxtNomeEditora.getText().trim(), jtxtEmailEditora.getText().trim(), jtxtCnpjEditora.getText().trim());
        listarTabelaEditora();
        jtxtId.setText("");
        jtxtNomeEditora.setText("");
        jtxtEmailEditora.setText("");
        jtxtCnpjEditora.setText("");
        nomeEditoraAtual = "";

    }//GEN-LAST:event_btnNovoActionPerformed

