    private void preencherCampos() {
        jtxtId.setText("" + jtableEditora.getValueAt(jtableEditora.getSelectedRow(), 0));
        jtxtNomeEditora.setText("" + jtableEditora.getValueAt(jtableEditora.getSelectedRow(), 1));
        jtxtCnpjEditora.setText("" + jtableEditora.getValueAt(jtableEditora.getSelectedRow(), 2));
        jtxtEmailEditora.setText("" + jtableEditora.getValueAt(jtableEditora.getSelectedRow(), 3));
        idEditoraAtual = Integer.parseInt(jtxtId.getText());
        nomeEditoraAtual = jtxtNomeEditora.getText();
    }

