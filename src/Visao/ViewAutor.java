    private void preencherCampos() {
        idAutorAtual = Integer.parseInt("" + jTableAutor.getValueAt(jTableAutor.getSelectedRow(), 0));
        txtNomeAutor.setText("" + jTableAutor.getValueAt(jTableAutor.getSelectedRow(), 1));
        nomeAutor = "" + jTableAutor.getValueAt(jTableAutor.getSelectedRow(), 1);
    }

    private void listarTabelaAutor() {
        CtrlAutor.listarAutor(jTableAutor);
    }

