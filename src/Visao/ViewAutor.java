    private void brnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_brnSalvarActionPerformed
        String mensagem = "Deseja salvar este Autor? \nNome: " + txtNomeAutor.getText();
        String titulo = "Salvar Autor";
        int resposta = JOptionPane.showConfirmDialog(null, mensagem, titulo, JOptionPane.YES_NO_OPTION);
        if (resposta == JOptionPane.YES_OPTION) {
            CtrlAutor.salvar(txtNomeAutor.getText().trim());
            listarTabelaAutor();
            txtNomeAutor.setText("");
        }
    }//GEN-LAST:event_brnSalvarActionPerformed

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        String mensagem = "Deseja Editar este Autor?\nNome: " + nomeAutor;
        String titulo = "Editar Autor";
        int resposta = JOptionPane.showConfirmDialog(null, mensagem, titulo, JOptionPane.YES_NO_OPTION);
        if (resposta == JOptionPane.YES_OPTION) {
            CtrlAutor.editar(txtNomeAutor.getText().trim(), nomeAutor, idAutorAtual);
            listarTabelaAutor();
            txtNomeAutor.setText("");
        }
    }//GEN-LAST:event_btnEditarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        String mensagem = "Deseja excluir este Autor?\nNome: " + nomeAutor;
        String titulo = "Excluir Autor";
        int resposta = JOptionPane.showConfirmDialog(null, mensagem, titulo, JOptionPane.YES_NO_OPTION);
        if (resposta == JOptionPane.YES_OPTION) {
            CtrlAutor.excluir(idAutorAtual, nomeAutor);
            listarTabelaAutor();
            txtNomeAutor.setText("");
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

